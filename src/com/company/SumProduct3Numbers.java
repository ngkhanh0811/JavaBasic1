package com.company;

public class SumProduct3Numbers {
    public static void main(String[] args){
        int number1, number2, number3, sum, product;
        number1 = 7;
        number2 = 8;
        number3 = 9;
        sum = number1 + number2 + number3;
        product = number1 * number2 * number3;
        System.out.println("The sum of three number is " +sum);
        System.out.println("The product of three number is " +product);
    }
}
