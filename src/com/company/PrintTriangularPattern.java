package com.company;

public class PrintTriangularPattern {
    public static void main(String[] args){
        System.out.print("*****");
        System.out.println();
        System.out.print(" ");
        System.out.print("*");
        System.out.print(" ");
        System.out.print("*");
        System.out.println();
        System.out.print(" ");
        System.out.print(" ");
        System.out.print("*");
    }
}
