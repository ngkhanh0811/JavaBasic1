package com.company;

public class PrintSquarePattern {
    public static void main(String[] args){
        int i;
        System.out.print("******");
        for(i=0;i<4;i++)
        {
            System.out.println();
            System.out.print("*");
            System.out.print(" ");
            System.out.print(" ");
            System.out.print(" ");
            System.out.print(" ");
            System.out.print("*");
        }
        System.out.println();
        System.out.print("******");
    }
}
